import time
# For Loop - a statement that will execute a block of code a limited amount of time
# while loop - unlimited
# for loop - limited

for i in range(10):
    print(i)

print()

for a in range(50,100):
    print(a)

print()

for j in range(50, 100+1):
    print(j)

print()

for k in range(50, 100+1,2):
    print(k)

print()

for b in "Vyapti Shah":
    print(b)

print()

for seconds in range(10,0,-1):
    print(seconds)
    time.sleep(1)
print("Happy New Year!")