#Type Casting - convert a data type of a value to another data type

x = 1 #int
y = 2.0 #float
z = "3" #str

print(x)
print(y)
print(z)

print()

print(int(y))

print()

print(z * 3)
print(int(z) * 3)

print()

print(float(x))

print()

print("value of x is " + str(x))
print("value of y is " + str(y))
print("value of z is " + str(z))